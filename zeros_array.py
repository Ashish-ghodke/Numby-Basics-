import numpy as np

# this is a array filled with zeros
# FOR ZEROS array we need to use np.zeros()


arr = np.zeros((3))
print(arr)  

arr1 = np.zeros((3,4))
print(arr1)


# we can also create a one's array

arr1 = np.ones((3,4))
print(arr1)

# empty array
# empty array will take all the previous data types of the array it is created from

arr_em = np.empty((3,4))
print(arr_em)


